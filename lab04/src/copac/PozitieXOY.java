package copac;

public class PozitieXOY {
    int x;
    int y;

    public PozitieXOY(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "PozitieXOY{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
